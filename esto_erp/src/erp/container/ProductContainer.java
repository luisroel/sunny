    private void localInit(User user, UserRight right) {
        this.productView = new ProductView(user, right);        
        this.pnlCatalog.setLayout(new java.awt.BorderLayout());
        this.pnlCatalog.add(productView);
        this.pnlCatalog.revalidate(); 
        
        Product selectedProduct;
        selectedProduct = productView.getSelectedItem();
        if (selectedProduct == null){
            txtCode.setText("");
            txtDescription.setText("");

            this.productComponentsView = new ComponentView(user, right, 0, new Long(-1));
            this.submaterialComponentView = new ComponentView(user, right, 1, new Long(-1));
            this.pricesView = new RangeView(user, right, 0, new Long(-1));
        } else {
            txtCode.setText(selectedProduct.getCode());
            txtDescription.setText(selectedProduct.getDescription());
            
            this.productComponentsView = new ComponentView(user, right, 0, selectedProduct.getId());
            this.submaterialComponentView = new ComponentView(user, right, 1, selectedProduct.getId());
            this.pricesView = new RangeView(user, right, 0, selectedProduct.getId());
        }
        
        this.pnlChildParts.setLayout(new java.awt.BorderLayout());
        this.pnlChildParts.add(this.productComponentsView);
        this.pnlChildParts.revalidate(); 
        
        this.pnlSubmaterials.setLayout(new java.awt.BorderLayout());
        this.pnlSubmaterials.add(this.submaterialComponentView);
        this.pnlSubmaterials.revalidate();
        
        this.pnlPrices.setLayout(new java.awt.BorderLayout());
        this.pnlPrices.add(this.pricesView);
        this.pnlPrices.revalidate();

        productView.getController().addSelectionListener(this);
    }

    @Override
    public void valueChanged(ListSelectionEvent lse) {
        ComponentController productComponentsController;
        ComponentController submaterialComponentsController;
        RangeController pricesController;
        Product selectedProduct;
         
        productComponentsController = productComponentsView.getController();
        submaterialComponentsController = submaterialComponentView.getController();
        pricesController = pricesView.getController();
        
        selectedProduct = productView.getSelectedItem();
        if (selectedProduct == null){
            txtCode.setText("");
            txtDescription.setText("");
            
            productComponentsController.setItem(new Long(-1));
            submaterialComponentsController.setItem(new Long(-1));
            pricesController.setItem(new Long(-1));
        } else {
            txtCode.setText(selectedProduct.getCode());
            txtDescription.setText(selectedProduct.getDescription());
            productComponentsController.setItem(selectedProduct.getId());
            submaterialComponentsController.setItem(selectedProduct.getId());
            pricesController.setItem(selectedProduct.getId());
        }
        
        productComponentsController.loadData();
        submaterialComponentsController.loadData();
        pricesController.loadData();
    }
